	private static final String STYLE_ICON_PREFIX = "icon-";
	private final String style;

	private IconType() {
		style = toString().toLowerCase().replaceAll("_", "-");
	}

	private IconType(String style) {
		this.style = style;
	}

	@Override
	public String get() {
		return IconType.STYLE_ICON_PREFIX + style;
	}

	public static IconType get(String iconName) {
		if (iconName == null) {
			return null;
		}
		try {
			String normalized = iconName;
			normalized = CaseFormat.UPPER_CAMEL.to(CaseFormat.UPPER_UNDERSCORE, "" + normalized);
			IconType iconType = IconType.valueOf(normalized);
			return iconType;
		}
		catch (IllegalArgumentException e) {
			return null;
		}
	}

